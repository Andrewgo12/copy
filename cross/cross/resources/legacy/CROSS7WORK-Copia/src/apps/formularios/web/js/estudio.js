function CalcularMuestraAle(sbForm,nuTipo)
{
	var nuZ = calcularZetaAle(sbForm.estudio__esalalphan.value);
	var nuN =  sbForm.estudio__esaltampobn.value;
	var nuE =  sbForm.estudio__esalprecisin.value;
	var nuUDC =  sbForm.estudio__esaludcn.value;
	var nuMues = 0;
	
	<!-- Aleatorio -->
	if(nuTipo==1)
	{
		<!-- Todos estos campos disparan la misma función, pero sólo retornará algo si son no nulos -->
		if(nuUDC!='' && nuE!='' && nuZ!='')
		{
			if(nuN!='')
			{
				<!-- N CONOCIDO:  [(N * z?) * (UDC / 5) * (1 – UDC / 5)] / [(E/5)? * (N-1)] + [z? * (UDC/5) * (1- UDC / 5)]  -->
				var nuA = ((nuN*nuZ*nuZ)*(nuUDC/5)*(1-nuUDC/5));
				var nuB = ((nuE/5)*(nuE/5)*(nuN-1)+nuZ*nuZ*(nuUDC/5)*(1-nuUDC/5));
				nuMues = Math.round(nuA/nuB);
			}
			else
			{
				<!-- N DESCONOCIDO:  [z? * (UDC/5) * (1 – UDC/5)] / (E/5)  -->
				var nuA = (nuZ * nuZ * (nuUDC / 5) * (1 - nuUDC / 5));
				var nuB =  ((nuE / 5) * (nuE / 5));
				nuMues = Math.round(nuA/nuB);
			}
		}
	}
	document.forms[0].estudio__estunumusuan.value = nuMues;
	document.forms[0].estudio__eseszetan.value = nuZ;
}

