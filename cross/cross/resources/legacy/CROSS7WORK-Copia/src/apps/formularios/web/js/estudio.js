function agregarRespuesta(objCheck,objHidden,sbValue)
{
	if(objCheck.checked==true)
	{
		if(objHidden.value)
			objHidden.value += ","+sbValue;
		else
			objHidden.value = sbValue;
	}
	else
	{
		objHidden.value = sustraerCadena(sbValue,objHidden.value,',');
	}
}

function sustraerCadena(sbNeeded,sbSubject,sbSep)
{
	var myString = sbSubject; 
	var myArray = myString.split(sbSep);
	var newArray = new Array();
	
	var nuLength = myArray.length;
	
	for(nuCont=0,nuAux=0;nuCont<nuLength;nuCont++)
	{
		if(myArray[nuCont] != sbNeeded)
			newArray[nuAux++] = myArray[nuCont];
	}
	return newArray.join(sbSep);
}

